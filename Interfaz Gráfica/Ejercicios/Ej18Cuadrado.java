package prog.ud13.ejercicios;

public class Ej18Cuadrado {
	private int x;
	private int y;
	
	
	public Ej18Cuadrado(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
}
