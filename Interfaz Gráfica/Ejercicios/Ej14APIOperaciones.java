package prog.ud13.ejercicios;

public class Ej14APIOperaciones {

	public int sumar(int op1, int op2) {
		return op1 + op2;
	}

	public int restar(int op1, int op2) {
		return op1 - op2;
	}
	
	public int multiplicar(int op1, int op2) {
		return op1 * op2;
	}
	
	public int dividir(int op1, int op2) {
		return op1 / op2;
	}
}
